package operators;

public class O001_Arithmatic {
	public static void main(String[] args) {
		
		int a = 15;
		int b = 2;
		
		System.out.println(a+b);
		System.out.println(a+"tops");
		System.out.println(a+b+"Tops");
		System.out.println(a+"tops"+b);
		System.out.println("tops"+a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
	}
}
